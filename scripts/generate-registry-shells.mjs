import { execFileSync } from "node:child_process";
import { mkdirSync, readdirSync, readFileSync, rmSync, rmdirSync, unlinkSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const rootDir = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const tempDir = join(rootDir, ".tmp-registry-shells");
const generatedMarker = "// AUTO-GENERATED BY npm run generate:registry-shells";

function toSymbol(name) {
  return name.replace(/[^a-zA-Z0-9]/g, "");
}

function clusterDirectory(item) {
  return item.clusterId.replace(`${item.tierId}-`, "");
}

function componentImportPath(item) {
  return `~/components/registry/${item.tierId}/${clusterDirectory(item)}/${item.slug}`;
}

function componentPath(item) {
  return join(rootDir, "src", "components", "registry", item.tierId, clusterDirectory(item), `${item.slug}.tsx`);
}

function routePath(item) {
  return join(rootDir, "src", "routes", `${item.slug}.tsx`);
}

function ensureParent(filePath) {
  mkdirSync(dirname(filePath), { recursive: true });
}

function writeIfChanged(filePath, nextContent) {
  ensureParent(filePath);

  let previousContent = null;

  try {
    previousContent = readFileSync(filePath, "utf8");
  } catch {}

  if (previousContent === nextContent) {
    return;
  }

  writeFileSync(filePath, nextContent);
}

function componentTemplate(item) {
  const symbol = toSymbol(item.name);

  return `${generatedMarker}
import { mergeProps, splitProps } from "solid-js";
import type { JSX } from "solid-js";
import { RegistryComponentShell } from "~/components/registry-component-shell";
import { registryItemBySlug } from "~/lib/registry";

export type ${symbol}Props = JSX.HTMLAttributes<HTMLElement> & {
  class?: string;
};

export function ${symbol}(userProps: ${symbol}Props) {
  const props = mergeProps(userProps);
  const [local, others] = splitProps(props, ["class", "children"]);

  return (
    <RegistryComponentShell item={registryItemBySlug["${item.slug}"]} class={local.class} {...others}>
      {local.children}
    </RegistryComponentShell>
  );
}
`;
}

function pageTemplate(item) {
  const symbol = toSymbol(item.name);

  return `${generatedMarker}
import { Title } from "@solidjs/meta";
import { RegistryPageShell } from "~/components/registry-page-shell";
import { ${symbol} } from "${componentImportPath(item)}";
import { registryItemBySlug } from "~/lib/registry";

export default function ${symbol}Page() {
  return (
    <>
      <Title>${item.name} | Stylyf</Title>
      <RegistryPageShell item={registryItemBySlug["${item.slug}"]}>
        <${symbol} />
      </RegistryPageShell>
    </>
  );
}
`;
}

function walkFiles(dirPath) {
  const results = [];

  for (const entry of readdirSync(dirPath, { withFileTypes: true })) {
    const nextPath = join(dirPath, entry.name);

    if (entry.isDirectory()) {
      results.push(...walkFiles(nextPath));
      continue;
    }

    results.push(nextPath);
  }

  return results;
}

function pruneEmptyDirectories(dirPath, stopPath) {
  for (const entry of readdirSync(dirPath, { withFileTypes: true })) {
    if (entry.isDirectory()) {
      pruneEmptyDirectories(join(dirPath, entry.name), stopPath);
    }
  }

  if (dirPath !== stopPath && readdirSync(dirPath).length === 0) {
    rmdirSync(dirPath);
  }
}

rmSync(tempDir, { recursive: true, force: true });
mkdirSync(tempDir, { recursive: true });

execFileSync(
  join(rootDir, "node_modules", ".bin", "tsc"),
  ["src/lib/registry.ts", "--module", "NodeNext", "--target", "ES2022", "--skipLibCheck", "--outDir", tempDir],
  { cwd: rootDir, stdio: "pipe" },
);

const compiledRegistryPath = join(tempDir, "registry.js");
const { registryItems } = await import(pathToFileURL(compiledRegistryPath).href);
const generatedComponentPaths = new Set();
const generatedRoutePaths = new Set();

for (const item of registryItems) {
  const nextComponentPath = componentPath(item);
  generatedComponentPaths.add(nextComponentPath);
  writeIfChanged(nextComponentPath, componentTemplate(item));

  if (item.registryShape.includes("page")) {
    const nextRoutePath = routePath(item);
    generatedRoutePaths.add(nextRoutePath);
    writeIfChanged(nextRoutePath, pageTemplate(item));
  }
}

for (const filePath of walkFiles(join(rootDir, "src", "components"))) {
  const content = readFileSync(filePath, "utf8");

  if (content.startsWith(generatedMarker) && !generatedComponentPaths.has(filePath)) {
    unlinkSync(filePath);
  }
}

for (const filePath of walkFiles(join(rootDir, "src", "routes"))) {
  const content = readFileSync(filePath, "utf8");

  if (content.startsWith(generatedMarker) && !generatedRoutePaths.has(filePath)) {
    unlinkSync(filePath);
  }
}

pruneEmptyDirectories(join(rootDir, "src", "components"), join(rootDir, "src", "components"));
pruneEmptyDirectories(join(rootDir, "src", "routes"), join(rootDir, "src", "routes"));

rmSync(tempDir, { recursive: true, force: true });
